# Logical operator -----------------> and, or, not
        # with boolean value
                                 #and
print(True and True)
print(True and False)
print(False and True)
print(False and False)

                                  # or
print(True or True)
print(True or False)
print(False or True)
print(False or False)

                                 # not
print(not True)
print(not False)

                                   # with non boolean value
print(12 and 10)
print("Samir" and 12)
print(0 and 12)


print("Samir" or "Maharana")